func IsEmail(str string) bool {
	regex, err := regexp.Compile(EmailRegexString)
	if err != nil {
		return false
	}

	return regex.MatchString(str)
}

func ParsePhoneNumber(phone string, defaultRegion string) (*phonenumbers.PhoneNumber, error) {
	return phonenumbers.Parse(phone, defaultRegion)
}

func FormatE164(phone string, defaultRegion string) (string, error) {
	num, err := ParsePhoneNumber(phone, defaultRegion)
	if err != nil {
		return "", err
	}

	return phonenumbers.Format(num, phonenumbers.E164), nil
}

func IsE164Format(phoneNumber string) bool {
	regex, err := regexp.Compile(E164RegexString)
	if err != nil {
		return false
	}

	if !regex.MatchString(phoneNumber) {
		return false
	}

	if strings.HasPrefix(phoneNumber, "+84") {
		numberPart := "0" + phoneNumber[3:]

		if len(numberPart) != 10 {
			return false
		}
	}
	return true
}
